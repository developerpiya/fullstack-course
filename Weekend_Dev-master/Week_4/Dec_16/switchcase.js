switch(10 + 5) {
    case 10:
        console.log("Case 1");
        console.log(10);
        break;
    
    case 11:
        console.log("Case 2");
        console.log(11);
        

    case 12:
        console.log("Case 3");
        console.log(12);
        break; // breaks you out of switch
    
    case 13:
        console.log("Case 4");
        console.log(13);
        break; // breaks you out of switch

    default:  // case when expression doesn't match anything
        console.log("no match");
}